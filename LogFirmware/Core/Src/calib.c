#ifndef CALDUMP_CHUNK_BYTES
#define CALDUMP_CHUNK_BYTES 64u   // safe for CDC FS
#endif

typedef struct {
    uint8_t active;
    uint8_t head;

    uint8_t stage;      // 0=header, 1=payload, 2=done
    uint16_t rec_bytes; // sizeof(LogLutPt_t)
    uint16_t n_pts;

    const uint8_t *ptr; // byte pointer into LUT
    uint32_t remaining; // bytes remaining
} CalDumpCtx_t;

static CalDumpCtx_t g_caldump = {0};

static uint8_t cdc_try_tx(const uint8_t *buf, uint16_t len)
{
    // Non-blocking: returns 1 if accepted, 0 if busy
    return (CDC_Transmit_HS((uint8_t*)buf, len) == USBD_OK) ? 1u : 0u;
}

void CALDUMP_Start(uint8_t head)
{
    LogLut_t lut;
    if (!LOGCAL_GetLUT(head, &lut)) {
        // If you want, you can queue an error string here via your normal send_line path.
        g_caldump.active = 0;
        return;
    }

    g_caldump.active = 1;
    g_caldump.head = head;
    g_caldump.stage = 0;
    g_caldump.rec_bytes = (uint16_t)sizeof(LogLutPt_t); // should be 6
    g_caldump.n_pts = lut.n_pts;
    g_caldump.ptr = (const uint8_t*)lut.pts;
    g_caldump.remaining = (uint32_t)lut.n_pts * (uint32_t)g_caldump.rec_bytes;
}

uint8_t CALDUMP_IsActive(void)
{
    return g_caldump.active;
}

/**
 * Call this repeatedly from main loop while state==CAL_DUMP.
 * This function never blocks waiting for USB; if USB is BUSY it just returns.
 */
void CALDUMP_Task(void)
{
    if (!g_caldump.active) return;

    // Stage 0: send ASCII header
    if (g_caldump.stage == 0u) {
        char hdr[96];
        int n = snprintf(hdr, sizeof(hdr),
                         "OK H%u N=%u RB=%u\r\n",
                         (unsigned)g_caldump.head,
                         (unsigned)g_caldump.n_pts,
                         (unsigned)g_caldump.rec_bytes);

        if (n < 0) { g_caldump.active = 0; return; }

        if (cdc_try_tx((const uint8_t*)hdr, (uint16_t)n)) {
            g_caldump.stage = 1u;
        }
        return;
    }

    // Stage 1: send binary payload in chunks
    if (g_caldump.stage == 1u) {
        if (g_caldump.remaining == 0u) {
            g_caldump.stage = 2u;
            return;
        }

        uint16_t chunk = (g_caldump.remaining > CALDUMP_CHUNK_BYTES)
                         ? (uint16_t)CALDUMP_CHUNK_BYTES
                         : (uint16_t)g_caldump.remaining;

        if (cdc_try_tx(g_caldump.ptr, chunk)) {
            g_caldump.ptr += chunk;
            g_caldump.remaining -= chunk;
        }
        return;
    }

    // Stage 2: send DONE line and stop
    if (g_caldump.stage == 2u) {
        const char done[] = "OK DONE\r\n";
        if (cdc_try_tx((const uint8_t*)done, (uint16_t)(sizeof(done) - 1u))) {
            g_caldump.active = 0u;
        }
        return;
    }
}


