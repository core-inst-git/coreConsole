int LOGCAL_GetLUT(uint8_t head, LogLut_t *out)
{
    if (!out) return 0;
    if (head == 0u || head > CAL_NUM_HEADS) return 0;

    out->pts = log_lut_common;
    out->n_pts = (uint16_t)(sizeof(log_lut_common) / sizeof(log_lut_common[0]));
    return 1;
}

